<template>
    <div class="toast" v-show="isShow">
        <div>{{message}}</div>
    </div>
</template>

<script>
export default {
    name: "Toast",
    /* props: {
        message: {
            type: String,
            default: ''
        },
        isShow: {
            type: Boolean,
            default: false
        }
    }, */
    data() {
        return {
            message: '',
            isShow: false
        }
    },
    components: {},
    methods: {
        show(message="默认文字", duration=2000) {
            console.log('-------');
            this.isShow = true;
            this.message = message

            setTimeout(() => {
                this.message = ''
                this.isShow = false
            }, duration)
        }
    }
}
</script>

<style scoped>
    .toast {
        position: fixed;
        top: 50%;
        left: 50%;
